                                      <h4 class="card-title">  The Kenyan SGR</h4>
                                     <p class="card-title-desc">The Mombasa–Nairobi Standard Gauge Railway is a standard-gauge railway (SGR) in Kenya that connects the large Indian Ocean city of Mombasa with Nairobi, the country's capital and largest city. This SGR runs parallel to the narrow-gauge Uganda Railway that was completed in 1901 under British colonial rule. The East African Railway Master Plan provides for the Mombasa–Nairobi SGR to link with other SGRs being built in the East African Community.[4]

At a cost of US$3.6 billion, the SGR was among Kenya's most expensive infrastructure projects as at the time it was launched. The prime contractor was the China Road & Bridge Corporation CRBC, which hired 25,000 Kenyans to work on the railway. CRBC's holding company, China Communications Construction Company is contracted to operate the line for its first five years..
                                         </p>
                                         <p class="card-title-desc">An extension from Nairobi to Suswa was completed in October 2019 extending the line's length to approximately 578.8 kilometres (359.6 mi).

The first fare-paying passengers boarded the "Madaraka Express" on Madaraka Day (1 June 2017), the 54th anniversary of Kenya's attainment of self-rule from Great Britain. Commercial freight services began on 1 January 2018.[6] Passenger uptake has exceeded expectations,[7] with the train carrying 2 million riders in the first 17 months of operation.[8] By November 2018, the SGR was operating 30 freight trains and 4 passenger trains per day.[9] double stack freight trains have run on the line since 2018..
Route

Mombasa-Nairobi Standard Gauge Railway Map
The Mombasa-Nairobi SGR generally runs parallel to the Uganda Railway, a metre-gauge that was built during British colonial rule.[11] The SGR, however, has a straighter alignment that accommodates higher speeds. Because of the rough and hilly terrain, large portions of the SGR were built on viaducts and embankments and in cuttings.

For example, the Uganda Railway tackled the hilly terrain near Mazeras township by using a spiral. In contrast, the SGR passes through this area on two bridges, with the 43.5 metres (143 ft) high Mazeras-2 bridge being the highest one on the route. As it approaches Nairobi, the SGR crosses the 2.6 kilometres (1.6 mi) Athi River Super Bridge, which at the time of its completion was the sixth-longest bridge in Africa. The SGR has a total of 98 bridges.[12]

Another purpose of the SGR's viaducts and embankments is environmental protection. The SGR passes through the transportation corridor between Tsavo East National Park and Tsavo West National Park, which is also host to the Nairobi–Mombasa Road and the Uganda Railway. Because the road and metre-gauge railway were built at ground level, collisions with wildlife can occur.[13] Viaducts and embankments elevate the SGR above ground level, with underpasses allowing wildlife to pass safely underneath.[13][14]

Passenger trains run between Mombasa Terminus in Miritini and Nairobi Terminus in Syokimau, near the Jomo Kenyatta International Airport.[15]
